export const generateReport = async (req, res) => {
  let browser = null;
  try {
    const { status = "All", startDate, endDate } = req.query;

    const query = {};
    if (status && status !== "All") {
      query.status = status;
    }

    if (startDate) {
      const start = new Date(startDate);
      if (!Number.isNaN(start.getTime())) {
        query.createdAt = { ...query.createdAt, $gte: start };
      }
    }

    if (endDate) {
      const end = new Date(endDate);
      if (!Number.isNaN(end.getTime())) {
        end.setHours(23, 59, 59, 999);
        query.createdAt = { ...query.createdAt, $lte: end };
      }
    }

    const orders = await Order.find(query)
      .populate("items.product address")
      .sort({ createdAt: -1 });

    const totalOrders = orders.length;
    const deliveredOrders = orders.filter((order) => order.status === "Delivered").length;
    const cancelledOrders = orders.filter((order) => order.status === "Cancelled").length;
    const totalRevenue = orders.reduce((sum, order) => sum + (order.amount || 0), 0);

    const html = buildHtmlReport({
      status,
      startDate,
      endDate,
      orders,
      totalOrders,
      deliveredOrders,
      cancelledOrders,
      totalRevenue,
    });

    // Launch browser with optimized settings for stability
    browser = await puppeteer.launch({
      headless: true,
      args: [
        '--no-sandbox',
        '--disable-setuid-sandbox',
        '--disable-dev-shm-usage',
        '--disable-accelerated-2d-canvas',
        '--no-first-run',
        '--no-zygote',
        '--disable-gpu',
        '--disable-web-security',
        '--disable-features=VizDisplayCompositor'
      ],
      timeout: 60000
    });

    const page = await browser.newPage();

    try {
      await page.setContent(html, { waitUntil: 'domcontentloaded' });

      // Wait for page to be fully loaded and stable
      await new Promise(resolve => setTimeout(resolve, 1000));

      const pdfBuffer = await page.pdf({
        format: 'A4',
        printBackground: true,
        margin: {
          top: '20px',
          right: '20px',
          bottom: '20px',
          left: '20px'
        },
        preferCSSPageSize: true
      });

      res.set({
        'Content-Type': 'application/pdf',
        'Content-Disposition': 'attachment; filename=BookHeaven_Report.pdf',
      });

      res.send(pdfBuffer);
    } finally {
      await page.close();
    }
  } catch (error) {
    console.error('Report generation error:', error);
    return res.status(500).json({ success: false, message: 'Unable to generate report' });
  } finally {
    if (browser) {
      await browser.close();
    }
  }
};
